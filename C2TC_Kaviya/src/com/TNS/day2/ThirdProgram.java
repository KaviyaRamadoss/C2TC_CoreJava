package com.TNS.day2;

public class ThirdProgram {
	public static void main(String args[])
	{
		//looping
		for (int i = 1; i <= 5; i++) {
            System.out.println("Iteration: " + i);
        }
		//while loop
		int i = 1;
        while (i <= 5) {
            System.out.println("numbers: " + i);
            i++;
        }
        //do while
        int j = 1;
        do {
            System.out.println("Integer: " + j);
            j++;
        } while (j <= 5);
	}

}
